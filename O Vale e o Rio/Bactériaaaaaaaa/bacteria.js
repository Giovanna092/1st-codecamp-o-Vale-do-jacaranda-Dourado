//primeira entrada
let plastico = 10;
let diasNecessarios = 0;


while(true){
    if(plastico <= 1){
        break;
    }else{
        plastico = plastico/2;
        diasNecessarios++;
    }
}
console.log(`Serão necessários ${diasNecessarios} dias`);

